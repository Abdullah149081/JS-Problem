let number1 = 45;
let number2 = 45;

let resultSum = number1 + number2;
console.log(resultSum);

// Substitution Math

let number3 = 5;
let resultSub = resultSum - number3;
console.log(resultSub);

// Multiplication Math

let number4 = 10;
let resultMul = resultSub * number4;
console.log(resultMul);

// Division Math

let number5 = 20;
let resultDiv = resultMul / number5;
console.log(resultDiv);

// percentage Math

let number6 = 10;
let resultPer = resultDiv % number6;
console.log(resultPer);
